"""
-> pop(index):  Removes and returns the element at the given index.
                If index not provided, removes the last element.
    E.g:    nums = [10, 20, 30]
            x = nums.pop()
            print(x) => 30
            print(nums) =>  [10, 20]
-> clear(): Removes all elements from the list.
    E.g:    x = [1, 2, 3]
            x.clear()
            print(x) => []
-> index(value): Returns the index of the first matching value.
    E.g:    x = ['a', 'b', 'c', 'd']
            print(x.index('b')) => 1
-> count(value): Returns the number of times a value appears.
    E.g:    x = [1, 2, 1, 3, 1]
            print(x.count(1)) => 3
"""